// 016 - 문자 두개 입출력

#include<stdio.h>
int main(void)
{
	char a, b; 
	puts("-----------------------------");
	puts(" 두 문자 입출력");
	puts("-------------------------");

	printf(" 문자 두 개 입력하세요 : ");
	scanf("%c%c", &a, &b);

	printf(" a = %c, b = %c \n", a, b);
	puts("-------------------------");
	puts("연속하면 괜찮은데, 첫 문자 입력 후 엔터를 치면 문제 발생합니다.");
	puts("-------------------------");

	return 0;
}